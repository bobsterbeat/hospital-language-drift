# FIGURE 4: Volcano — log2 fold-change vs bootstrap significance
# ---------------------------------------------------------------------------
# Compute per-term Mann-Whitney p-value (early vs late doc-level rates)
pvals = []
for _, row in tp_pivot.iterrows():
    term = row["term"]; cat = row["category"]
    early_vals = both[(both["term"] == term) & (both["category"] == cat) &
                      (both["period"] == "early (2010-2016)")]["count"].values.astype(float)
    late_vals  = both[(both["term"] == term) & (both["category"] == cat) &
                      (both["period"] == "late (2017-2025)")]["count"].values.astype(float)
    if len(early_vals) >= 3 and len(late_vals) >= 3:
        _, p = stats.mannwhitneyu(early_vals, late_vals, alternative="two-sided")
    else:
        p = 1.0
    pvals.append(p)

tp_pivot["pval"] = pvals
tp_pivot["neg_log10_p"] = -np.log10(np.clip(tp_pivot["pval"], 1e-10, 1))
tp_pivot.to_csv(REPORT / "term_fold_changes.csv", index=False)

fig, ax = plt.subplots(figsize=(8.5, 6))
for _, row in tp_pivot.iterrows():
    color = RED if row["category"] == "operational" else NAVY
    sig   = row["neg_log10_p"] > -np.log10(0.05)
    ax.scatter(row["log2fc"], row["neg_log10_p"],
               color=color, s=60, alpha=0.8,
               edgecolor="black" if sig else "none", linewidth=0.6, zorder=3)
    if sig and (abs(row["log2fc"]) > 0.5 or row["neg_log10_p"] > 2):
        ax.annotate(row["term"], (row["log2fc"], row["neg_log10_p"]),
                    textcoords="offset points", xytext=(4, 2), fontsize=6.5)

ax.axhline(-np.log10(0.05), color="gray", linestyle="--", linewidth=0.8,
           label="p = 0.05")
ax.axvline(0, color="black", linewidth=0.5)
ax.set_xlabel("Log₂ fold-change (late / early)")
ax.set_ylabel("−log₁₀(p)  [Mann-Whitney, two-sided]")
ax.set_title("Per-term drift: late period vs early period\n"
             "(outlined points = p < 0.05)")
ax.legend(handles=[
    Patch(facecolor=RED,  label="operational"),
    Patch(facecolor=NAVY, label="relational"),
    plt.Line2D([0],[0], color="gray", ls="--", lw=1, label="p = 0.05"),
], loc="upper left", frameon=False, fontsize=8)
