# Term-level long tables
def term_counts_df(docs, kind):
    rows = []
    col = "op_counts" if kind == "op" else "rel_counts"
    for _, d in docs.iterrows():
        for term, c in d[col].items():
            rows.append({"system":d["system"],"year":d["year"],"period":d["period"],
                         "doctype":d["doctype"],"term":term,"count":c,
                         "n_tokens":d["n_tokens"]})
    return pd.DataFrame(rows)

op_long  = term_counts_df(df, "op");  op_long["category"]  = "operational"
rel_long = term_counts_df(df, "rel"); rel_long["category"] = "relational"
both     = pd.concat([op_long, rel_long], ignore_index=True)

term_period = both.groupby(["category","term","period"]).agg(
    count=("count","sum"), tokens=("n_tokens","sum")).reset_index()
term_period["rate_per_10k"] = 10000 * term_period["count"] / term_period["tokens"].clip(1)
term_period.to_csv(REPORT / "term_rates_by_period.csv", index=False)

