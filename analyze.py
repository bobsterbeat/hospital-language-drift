def weighted_rate(g, count_col, token_col):
    return 10000 * g[count_col].sum() / max(g[token_col].sum(), 1)

def bootstrap_ci(values, n_boot=1000, ci=0.95):
    if len(values) == 0:
        return np.nan, np.nan
    boots = np.random.choice(values, size=(n_boot, len(values)), replace=True).mean(axis=1)
    lo = np.percentile(boots, (1 - ci) / 2 * 100)
    hi = np.percentile(boots, (1 + ci) / 2 * 100)
    return lo, hi

