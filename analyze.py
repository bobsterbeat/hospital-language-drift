# FIGURE 3: Heatmap — terms × year-buckets, z-score within term
# ---------------------------------------------------------------------------
year_buckets = [
    ("2010-2013", (2010, 2013)), ("2014-2016", (2014, 2016)),
    ("2017-2019", (2017, 2019)), ("2020-2022", (2020, 2022)),
    ("2023-2025", (2023, 2025)),
]

def bucket_rate(term_name, kind, lo, hi):
    sub = both[(both["term"] == term_name) &
               (both["category"] == kind) &
               (both["year"] >= lo) & (both["year"] <= hi)]
    if sub.empty or sub["n_tokens"].sum() == 0:
        return 0.0
    return 10000 * sub["count"].sum() / sub["n_tokens"].sum()

all_terms_sorted = (
    [("operational", t) for t in sorted(OPERATIONAL_TERMS.keys())] +
    [("relational",  t) for t in sorted(RELATIONAL_TERMS.keys())]
)

matrix = np.zeros((len(all_terms_sorted), len(year_buckets)))
for i, (cat, term) in enumerate(all_terms_sorted):
    for j, (_, (lo, hi)) in enumerate(year_buckets):
        matrix[i, j] = bucket_rate(term, cat, lo, hi)

# Z-score within each term (row), ignore all-zero rows
with np.errstate(invalid="ignore", divide="ignore"):
    row_std = matrix.std(axis=1, keepdims=True)
    row_std[row_std == 0] = 1
    z = (matrix - matrix.mean(axis=1, keepdims=True)) / row_std
z = np.nan_to_num(z, nan=0)

term_labels = [f"{'[OP] ' if cat == 'operational' else '[REL] '}{t}"
               for cat, t in all_terms_sorted]
bucket_labels = [b[0] for b in year_buckets]

fig, ax = plt.subplots(figsize=(8, 12))
im = ax.imshow(z, aspect="auto", cmap="RdBu_r", vmin=-2, vmax=2)
ax.set_xticks(range(len(bucket_labels)))
ax.set_xticklabels(bucket_labels, rotation=30, ha="right")
ax.set_yticks(range(len(term_labels)))
ax.set_yticklabels(term_labels, fontsize=7)
plt.colorbar(im, ax=ax, label="Z-score within term", shrink=0.6)
# Draw separator between op and rel sections
