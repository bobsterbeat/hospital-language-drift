# ---------------------------------------------------------------------------
# Mixed-effects model (statsmodels)
# ---------------------------------------------------------------------------
try:
    import statsmodels.formula.api as smf

    # Prepare: one row per doc with binary category indicator
    long_rows = []
    for _, d in df.iterrows():
        for category, rate_col in [("operational","op_rate_per_10k"),
                                   ("relational","rel_rate_per_10k")]:
            long_rows.append({
                "rate":     d[rate_col],
                "year":     d["year"] - 2010,   # centre at 2010
                "category": 1 if category == "operational" else 0,
                "system":   d["system"],
                "doctype":  d["doctype"],
            })
    ldf = pd.DataFrame(long_rows)
    ldf = ldf.dropna(subset=["rate"])

    model = smf.mixedlm(
        "rate ~ year * category",
        ldf,
        groups=ldf["system"],
    )
    result = model.fit(reml=True)
    summary_text = str(result.summary())
    (REPORT / "model_results.txt").write_text(summary_text)
    print("\nMixed-effects model fitted. Key coefficients:")
    print(result.summary().tables[1])
except Exception as exc:
    print(f"Mixed-effects model failed: {exc}")
    (REPORT / "model_results.txt").write_text(f"Model failed: {exc}\n")

